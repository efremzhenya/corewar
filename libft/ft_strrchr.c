#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	long int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while ((s[i] != c) && (i >= 0))
		i--;
	if (i == -1)
		return (NULL);
	return ((char*)&s[i]);
}
